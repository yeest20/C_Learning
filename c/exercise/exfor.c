#include<stdio.h>
int main(){
// for循环实现输出斐波那契数列前20项值
// for的（）表达式很有意思  
    for(int i = 0, fn1 = 1, fn2 = 0;i < 20;i++, fn1 = fn1 + fn2, fn2 = fn1 - fn2){ 
        printf("F[%d] = %d\n", i, fn1);
    }
}
